namespace Timetabio\Frontend\Commands
{
    use Timetabio\Frontend\Gateways\ApiGateway;

    class DeleteFeedUserCommand
    {
        /**
         * @var ApiGateway
         */
        private $apiGateway;

        public function __construct(ApiGateway $apiGateway)
        {
            $this->apiGateway = $apiGateway;
        }

        public function execute(string $feedId, string $userId)
        {
            return $this->apiGateway->deleteFeedUser($feedId, $userId)->unwrap();
        }
    }
}
